a = int(input('Перше число: '))
b = int(input('Друге число: '))


def calcu(a, b):

    r = a + b
    m = a - b
    n = a * b
    x = float(a / b)

    print('Результат додавання чисел ', a, 'та', b, '=', r)
    print('Результат віднімання чисел ', a, 'та', b, '=', m)
    print('Результат множення чисел ', a, 'та', b, '=', n)
    print('Результат ділення чисел ', a, 'та', b, '=', x)

calcu(a, b)





